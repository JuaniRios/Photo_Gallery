import pytest
from flaskr.objects.sexa_to_dec import sexa_to_dec


@pytest.mark.parametrize('inp, out', [
    (('N', (48, 24, 34.18236), 'E', (15, 37, 4.92671)), (48.4094, 15.6180)),
    (('N', (48, 11, 56.1044), 'E', (11, 22, 15.1060)), (48.1989, 16.3708)),
    (('N', (48, 22, 52.7381), 'E', (15, 32, 31.5489)), (48.3813, 15.5420)),
    (('N', (47, 14, 7.17032), 'E', (14, 34, 57.9153)), (47.2353, 14.5827)),
    (('N', (77, 30, 32.2314), 'W', (164, 45, 115.3812)), (77.5089, -164.754)),
    (('S', (71, 30, 29.1704), 'W', (151, 45, 7.3912)), (-71.508, -151.75)),
    (('S', (56, 30, 20.3918), 'E', (136, 45, 26.1926)), (-56.505, 136.757))


])
def test_sexa_to_dec(inp, out):
    lat, lon = sexa_to_dec(inp[0], inp[1], inp[2], inp[3])
    lat, lon = float(str(lat)[:7]), float(str(lon)[:7])
    assert lat == out[0], lon == out[1]
